public class DataTypes {
    public static void main(String[] args) {
        int i = 10;
        float f = 234.5f;
        char c = 'A';
        boolean b = true;
        double d = 6.99;
        System.out.println(i);
        System.out.println(f);
        System.out.println(b);
        System.out.println(d);
        System.out.println(c);
        
    }

}

